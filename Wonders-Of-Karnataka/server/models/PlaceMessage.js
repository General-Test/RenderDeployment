import mongoose from 'mongoose';

const placeSchema = mongoose.Schema({
    title: String,
    message: String,
    name: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likes: { type: [String], default: [] },
    comments: { type: [String], default: [] },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

const PlaceMessage = mongoose.model('PlaceMessage', placeSchema);

export default PlaceMessage;